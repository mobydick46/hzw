function getAchapter(index,flag){
		return "<div class=\"chapter\">\n"
		+"<a href=\"chapter/"+index+".html\" target=\"_blank\">"+(flag==0?"":index)+"  "+getTitle(index)+"</a>\n"
		+"</div>"
	}

	function loadChapters(index){
		var cpcontent="";
		if(index==0){
			prefix="Vol_"
			for(var i=1;i<=35;i++){
				var index=String(1000+i).substring(1);
				cpcontent+=getAchapter(prefix+index,0)
			}
		}
		else if(index==1){
			for(var i=337;i<=400;i++){
				cpcontent+=getAchapter(i)
			}
		}
		else if(index>=2&&index<=5){
			for(var i=index*100+201;i<=index*100+300;i++){
				cpcontent+=getAchapter(i)
			}
		}
		else{
			for(var i=801;i<=849;i++){
				cpcontent+=getAchapter(i)
			}
		}

		var chapterbox="<div id=\"chapterbox\">\n"
			+cpcontent+"\n</div>"
		$("#chapterbox").replaceWith(chapterbox);
	}

